#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*temp;
	t_list	*iter;

	iter = *lst;
	while (iter != NULL)
	{
		temp = iter -> next;
		del(iter -> content);
		free(iter);
		iter = temp;
	}
	*lst = NULL;
}
